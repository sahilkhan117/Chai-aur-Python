# Definition
# Booleans are a data type that represents one of two values: True or False.

# Use Cases
# - Booleans are commonly used in conditional statements and control flow.
# - They are essential for making decisions in programming.

# Boolean Operations
true_value = True
false_value = False

# Example of Arithmetic with Booleans
result = true_value + 4  # 5

# Additional Examples
# Comparison Operations
is_greater = 5 > 3  # True
is_equal = (5 == 5)  # True

print(f"Result of True + 4: {result}")
print(f"Is 5 greater than 3? {is_greater}")
print(f"Is 5 equal to 5? {is_equal}")
